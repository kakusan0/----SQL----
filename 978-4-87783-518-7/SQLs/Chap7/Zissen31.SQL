CREATE VIEW ORDER_SUMMARY (CUSTOMER_NAME, PRODUCT_NAME, PRICE, QUANTITY, TOTAL)
AS SELECT CUSTOMER_NAME, PRODUCT_NAME, PRICE, QUANTITY, PRICE*QUANTITY
FROM ORDER_LIST, PRODUCT_LIST, CUSTOMER_LIST
WHERE ORDER_LIST.CUSTOMER_CODE = CUSTOMER_LIST.CUSTOMER_CODE
AND ORDER_LIST.PRODUCT_CODE = PRODUCT_LIST.PRODUCT_CODE
